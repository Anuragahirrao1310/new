# Import the math module for scientific functions
import math

# Calculate the square root of a number
num = 16
square_root = math.sqrt(num)
print("Square root of", num, "is", square_root)

# Calculate the sine of an angle in radians
angle = math.radians(45)
sine_value = math.sin(angle)
print("Sine value of", angle, "radians is", sine_value)

# Calculate the logarithm base 10 of a number
number = 100
log_value = math.log10(number)
print("Logarithm base 10 of", number, "is", log_value)
